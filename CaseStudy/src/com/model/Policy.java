package com.model;

import java.util.Date;

public abstract class Policy {
	private int customerId;
	private String policyId;
	private String policyType;
	private String policyCommDate;
	private String policyEndDate;

	private String paymentMode;
	private String paymentFreq;
	private Double premiumAmount;
	private String policyRefNum;

	public int getCustomerId() {
		return customerId;
	}

	public void setCustomerId(int customerId) {
		this.customerId = customerId;
	}

	public String getPolicyId() {
		return policyId;
	}

	public void setPolicyId(String policyId) {
		this.policyId = policyId;
	}

	public String getPolicyType() {
		return policyType;
	}

	public void setPolicyType(String policyType) {
		this.policyType = policyType;
	}

	// String Commencement Edit
	public String getPolicyCommDate() {
		return policyCommDate;
	}

	public void setPolicyCommDate(String policyCommDate) {
		this.policyCommDate = policyCommDate;
	}

	public String getPolicyEndDate() {
		return policyEndDate;
	}

	public void setPolicyEndDate(String policyEndDate) {
		this.policyEndDate = policyEndDate;
	}

	public void setPaymentMode(String paymentMode) {
		this.paymentMode = paymentMode;
	}

	public String getPaymentMode() {
		return paymentMode;
	}

	public void setPaymentFreq(String paymentFreq) {
		this.paymentFreq = paymentFreq;
	}

	public String getPaymentFreq() {
		return paymentFreq;
	}

	public Double getPremiumAmount() {
		return premiumAmount;
	}

	public void setPremiumAmount(Double premiumAmount) {
		this.premiumAmount = premiumAmount;
	}

	public String getPolicyRefNum() {
		return policyRefNum;
	}

	public void setPolicyRefNum(String policyRefNum) {
		this.policyRefNum = policyRefNum;
	}

}
